// Create a file named if-statement.js.

// In that file, declare a variable named fruit.

// Make the fruit variable reference the value orange.

// Then use console.log() to print The fruit name has more than five characters. 
// if the length of the value of fruit is greater than five.
// Print The fruit name has less or equal than five characters. otherwise.

var fruit = 'orange';

if (fruit.length > 5)
{
  console.log('The fruit name has more than five characters.')
}
else
{
  console.log('The fruit name has less or equal than five characters.')
}